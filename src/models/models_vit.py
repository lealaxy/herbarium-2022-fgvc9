from functools import partial

import timm.models.vision_transformer
import torch
import torch.nn as nn


class VisionTransformer(timm.models.vision_transformer.VisionTransformer):
    """ Vision Transformer with support for global average pooling
    """

    def __init__(self, global_pool=False, genus=2564, institution=58, **kwargs):
        super(VisionTransformer, self).__init__(**kwargs)

        self.global_pool = global_pool
        if self.global_pool:
            norm_layer = kwargs['norm_layer']
            embed_dim = kwargs['embed_dim']
            self.fc_norm = norm_layer(embed_dim)

            del self.norm  # remove the original norm
        # 定义科属种分类头
        self.institution_depth = int(len(self.blocks) / 3)
        self.genus_depth = int((len(self.blocks) / 3) * 2)
        self.genus_head = nn.Linear(self.embed_dim, genus)
        self.institution_head = nn.Linear(self.embed_dim, institution)

    def forward(self, x):
        B = x.shape[0]
        x = self.patch_embed(x)

        # stole cls_tokens impl from Phil Wang, thanks
        cls_tokens = self.cls_token.expand(B, -1, -1)
        x = torch.cat((cls_tokens, x), dim=1)
        x = x + self.pos_embed
        x = self.pos_drop(x)

        for index in range(self.institution_depth):
            x = self.blocks[index](x)
        institution = self.institution_head(x[:, 0])

        for index in range(self.institution_depth, self.genus_depth):
            x = self.blocks[index](x)
        genus = self.genus_head(x[:, 0])

        for index in range(self.genus_depth, len(self.blocks)):
            x = self.blocks[index](x)

        if self.global_pool:
            x = x[:, 1:, :].mean(dim=1)  # global pool without cls token
            outcome = self.fc_norm(x)
        else:
            x = self.norm(x)
            outcome = x[:, 0]

        category = self.head(outcome)
        return category, genus, institution


def vit_base_patch16(**kwargs):
    model = VisionTransformer(
        patch_size=16, embed_dim=768, depth=12, num_heads=12, mlp_ratio=4, qkv_bias=True,
        norm_layer=partial(nn.LayerNorm, eps=1e-6), **kwargs)
    return model


